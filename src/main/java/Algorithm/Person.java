package Algorithm;

import java.util.Date;

public class Person {
  private String name;
  private Date birthDate;

  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  Date getBirthDate() {
    return birthDate;
  }
  public void setBirthDate(Date birthDate) {
    this.birthDate = birthDate;
  }
}
